import type { Quarter } from '@/types'
import { quarterImageUrl } from './quarter-images'

function q(
  id: number,
  cityId: number,
  citySlug: string,
  cityName: string,
  name: string,
  slug: string,
  description: string,
  opts?: { population?: number; area_km2?: number },
): Quarter {
  return {
    id,
    city_id: cityId,
    city_slug: citySlug,
    city_name: cityName,
    name,
    slug,
    description,
    image_url: quarterImageUrl(citySlug, slug),
    population: opts?.population ?? 0,
    area_km2: opts?.area_km2 ?? 0,
    property_count: 0,
  }
}

